list = [0 , 8 , 9 , 6 , 8 , 7 ]

# Sort 
list.sort()
print(list)

# Reverse
list.reverse()
print(list)


# Append
list.append(4)      # Adds 4 at the end 
print(list)


# Insert 
list.insert(4,2)        # Inserts '2'at 4th location 
print(list)

# Pop 
list.pop(5)
print(list)

# Remove 
list.remove(7)
print(list)
