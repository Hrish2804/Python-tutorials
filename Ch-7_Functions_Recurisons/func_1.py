# Create a function to input 5 marks and then calculate the percentage 

def perc(marks) :
        per = (marks[0] + marks[1] + marks[2] + marks[3] + marks[4])/5
        return per


marks1 = [45, 45 ,56, 49, 77]
percentage1 = perc(marks1)
print(percentage1)

marks2 = [99 , 8 , 78 , 99 , 66 ]
percentage2 = perc(marks2)
print(percentage2)





